const std = @import("std");

pub const Color = struct {
    red: u8,
    green: u8,
    blue: u8,
};

pub const Face = struct {
    const boldVal = [2][]const u8{ "\x1b[1m", "\x1b[22m" };
    const italicVal = [2][]const u8{ "\x1b[3m", "\x1b[23m" };
    const underlineVal = [2][]const u8{ "\x1b[4m", "\x1b[24m" };
    const strikeVal = [2][]const u8{ "\x1b[9m", "\x1b[29m" };
    const overlineVal = [2][]const u8{ "\x1b[53m", "\x1b[55m" };
    fg: Color,
    bg: Color,
    bold: bool,
    //\x1b[1m | 22m
    italic: bool,
    //\x1b]3m | 23m
    underline: bool,
    //\x1b[4m | 24m
    strike: bool,
    //\x1b[9m | 29m
    overline: bool,
    //\x1b[53m | 55m
};

pub const ColorSet = struct {
    const defaultFG = Color{
        .red = 0,
        .green = 0,
        .blue = 0,
    };
    const defaultBG = Color{
        .red = 254,
        .green = 254,
        .blue = 254,
    };
    const FaceSet = struct {
        fg: []const u8,
        bg: []const u8,
        bold: bool,
        italic: bool,
        underline: bool,
        strike: bool,
        overline: bool,
    };
    colors: std.StringArrayHashMap(Color),
    faces: std.StringArrayHashMap(FaceSet),

    pub fn init(alloc: std.mem.Allocator) ColorSet {
        return .{
            .colors = std.StringArrayHashMap(Color).init(alloc),
            .faces = std.StringArrayHashMap(FaceSet).init(alloc),
        };
    }
    pub fn addDefaultColors(self: *ColorSet) !void {
        for (defaultColors) |c| {
            try self.setColor(c.name, .{ .red = c.red, .green = c.green, .blue = c.blue });
        }
        for (defaultFaces) |face| {
            _ = self.getColor(face.fg) orelse continue;
            _ = self.getColor(face.bg) orelse continue;
            try self.faces.put(face.name, .{
                .fg = face.fg,
                .bg = face.bg,
                .bold = face.bold,
                .italic = face.italic,
                .underline = face.underline,
                .strike = face.strike,
                .overline = face.overline,
            });
        }
        for (defaultDiffFaces) |face| {
            var base = self.faces.get(face.baseName) orelse continue;
            if (face.fg) |fg| {
                base.fg = fg;
            }
            if (face.bg) |bg| {
                base.bg = bg;
            }
            if (face.bold) |bold| {
                base.bold = bold;
            }
            if (face.italic) |italic| {
                base.italic = italic;
            }
            if (face.underline) |underline| {
                base.underline = underline;
            }
            if (face.strike) |strike| {
                base.strike = strike;
            }
            if (face.overline) |overline| {
                base.overline = overline;
            }
            if (face.inverseColor) {
                var tmpfg = base.fg;
                base.fg = base.bg;
                base.bg = tmpfg;
            }
            try self.faces.put(face.name, base);
        }
    }
    pub fn printAllColors(self: *ColorSet) void {
        var keys = self.colors.keys();
        for (keys) |key| {
            var color = self.colors.get(key) orelse continue;
            std.log.info("\x1b[38;2;{};{};{}m {s} \x1b[0m", .{
                color.red,
                color.green,
                color.blue,
                key,
            });
        }
        var face_keys = self.faces.keys();
        for (face_keys) |key| {
            var face = self.faces.get(key) orelse continue;
            var bgcolor = self.colors.get(face.bg) orelse continue;
            var fgcolor = self.colors.get(face.fg) orelse continue;
            std.log.info("\x1b[38;2;{};{};{}m\x1b[48;2;{};{};{}m{s}{s}{s}{s}{s}{s}\x1b[0m", .{
                fgcolor.red,
                fgcolor.green,
                fgcolor.blue,
                bgcolor.red,
                bgcolor.green,
                bgcolor.blue,
                Face.boldVal[if (face.bold) 0 else 1],
                Face.italicVal[if (face.italic) 0 else 1],
                Face.underlineVal[if (face.underline) 0 else 1],
                Face.strikeVal[if (face.strike) 0 else 1],
                Face.overlineVal[if (face.overline) 0 else 1],
                key,
            });
        }
    }

    pub fn deinit(self: *ColorSet) void {
        self.colors.deinit();
        self.faces.deinit();
    }

    pub fn getColor(self: *ColorSet, id: []const u8) ?Color {
        return self.colors.get(id);
    }
    pub fn setColor(self: *ColorSet, id: []const u8, color: Color) !void {
        try self.colors.put(id, color);
    }

    pub fn setFace(self: *ColorSet, id: []const u8, fg: []const u8, bg: []const u8) !void {
        try self.faces.put(id, .{ .fg = fg, .bg = bg });
    }
    pub fn getFace(self: *ColorSet, id: []const u8) ?Face {
        const faceSet = self.faces.get(id) orelse return null;
        const fg = self.colors.get(faceSet.fg) orelse return null;
        const bg = self.colors.get(faceSet.bg) orelse return null;
        return Face{
            .fg = fg,
            .bg = bg,
        };
    }
};

